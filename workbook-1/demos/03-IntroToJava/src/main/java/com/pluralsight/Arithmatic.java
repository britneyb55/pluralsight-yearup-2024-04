package com.pluralsight;

public class Arithmatic
{
    public static void main(String[] args)
    {
        //type casting
        // Expression
        // An Expression is a calculation that results in a single value
        // 5 + 4 = 9 // one expression
        // 5 + 4 - 3 => 9 - 3 => 6

        // Statement
        // A statement is the whole action and contains one or more expressions
        // int sum = 5 + 4;

        // addition
        int sum = 5 + 4;
        System.out.println("The sum of 5 + 4 = " + sum); //sout

        // division
        int quotient = 5/2;
        System.out.println("5 / 2 = " + quotient);

        // double
        double quotientDouble = 5.0/2;
        System.out.println("5 / 2 = " + quotientDouble);
    }
}
